fn create_ltc_mag_lut() -> Image {
    // 11x11 RGBA32F
    // This is a placeholder for the diffuse magnitude LUT.
    // For Lambertian, it's simpler, but for Burley/Disney we use a LUT.
    // For now, providing a Lambertian-compatible identity-ish LUT.
    const SIZE: u32 = 11;
    let mut data = vec![0.0f32; SIZE as usize * SIZE as usize * 4];
    for y in 0..SIZE {
        for x in 0..SIZE {
            let i = (y * SIZE + x) as usize * 4;
            data[i] = 1.0; // Magnitude
            data[i + 1] = 0.0; // Fresnel-like or other param
        }
    }

    let mut image = Image::new(
        Extent3d {
            width: SIZE,
            height: SIZE,
            depth_or_array_layers: 1,
        },
        TextureDimension::D2,
        bytemuck::cast_slice(&data).to_vec(),
        TextureFormat::Rgba32Float,
        RenderAssetUsages::RENDER_WORLD,
    );
    image.sampler = ImageSampler::linear();
    image
}

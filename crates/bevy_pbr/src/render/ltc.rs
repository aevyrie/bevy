pub struct LtcPlugin;

impl Plugin for LtcPlugin {
    fn build(&self, app: &mut App) {
        if !app.world().is_resource_added::<LtcLuts>() {
            let mut images = app.world_mut().resource_mut::<Assets<Image>>();
            let ltc_luts = LtcLuts {
                ltc_1: images.add(create_ltc_lut_1()),
                ltc_2: images.add(create_ltc_lut_2()),
                ltc_mag: images.add(create_ltc_mag_lut()),
            };
            app.insert_resource(ltc_luts);
        }

        app.add_plugins(ExtractResourcePlugin::<LtcLuts>::default());
    }
}

